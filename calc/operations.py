"""Basic math operations."""

def add(a, b):
    """Add a and b."""
    return int(a) + int(b)

def sub(a, b):
    """Substract b from a."""

    return int(a) - int(b)

def mult(a, b):
    """Multiply a and b."""

    return int(a) * int(b)

def div(a, b):
    """Divide a by b."""

    return int(a) / int(b)
